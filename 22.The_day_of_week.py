day_week = int(input('Введите число от 1 до 7..'))
if day_week == 1:
    print('Это понедельник!')
elif day_week == 2:
    print('Это вторник')
elif day_week == 3:
    print('Это среда')
elif day_week == 4:
    print('Это четверг')
elif day_week == 5:
    print('Это пятница')
elif day_week == 6:
    print('Это суббота')
elif day_week == 7:
    print('Это воскресенье')
else:
    print('Error')
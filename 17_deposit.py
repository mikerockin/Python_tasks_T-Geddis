first_money = int(input('Введите первоначальную сумму внесенную на сберегательный счет: '))
per_bet = (int(input('Введите годовую процентную ставку начисляемую на остаток счета: '))/100)
frequency = int(input('Введите частоту начисления процентного дохода в год (например, если проценты начисляются ежемесячно, то ввести 12, если процентный доход начисляется ежеквартально, то внести 4: '))
years = int(input('Введите количество лет, в течение которых сберегательный счет будет зарабатывать процентный доход: '))
end_money = first_money * (1 + per_bet / frequency)** (frequency * years)
print(end_money)
print(per_bet)
